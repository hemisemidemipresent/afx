function getColorFromMargin(margin, isRepublicanWinner) {
    if (!isRepublicanWinner) {
        if (margin > 15) return colors[0];
        if (margin > 5) return colors[1];
        if (margin > 1) return colors[2];
        return colors[3];
    }
    if (margin < 1) return colors[4];
    if (margin < 5) return colors[5];
    if (margin < 15) return colors[6];
    return colors[7];
}

function mouseEnter(i, id) {
    let results = data[index];

    let countyres = results[i];
    let rep = (countyres[1].candidatevotes / countyres[1].totalvotes) * 100; // republican percentage
    let dem = (countyres[0].candidatevotes / countyres[0].totalvotes) * 100; // democratic percentage
    let diff = Math.max(rep, dem) - Math.min(rep, dem);
    let isRepublicanWinner = rep > dem;

    let color = getColorFromMargin(diff, isRepublicanWinner);
    let element = document.getElementById(counties[i]);
    element.style.fill = color;
    console.log(id);
    document.getElementById('county').innerText = `${id} county`;
    document.getElementById('diff').innerText = `${
        isRepublicanWinner ? 'Rep' : 'Dem'
    } +${Math.abs(Math.round(diff * 10) / 10)}`;
    document.getElementById(
        'population'
    ).innerText = `population: ${countyres[0].totalvotes}`;
    document.getElementById('diff').style.color = isRepublicanWinner
        ? colors[6]
        : colors[1];

    document.getElementById(
        'dem'
    ).innerText = `${countyres[0].candidate}: ${countyres[0].candidatevotes}`;
    document.getElementById(
        'rep'
    ).innerText = `${countyres[1].candidate}: ${countyres[1].candidatevotes}`;
}
